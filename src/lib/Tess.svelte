<script>
	import { onMount } from 'svelte';
    import { tweened } from 'svelte/motion';
	import { cubicOut } from 'svelte/easing';

	import Tesseract from 'tesseract.js';

	let div;
	let input;
	let w;
	let h;
	let input_overlay;
	let ioctx;
    let showImage = false;
    // let percentage = 0;

    let processButton;

    const percentage = tweened(0, {
		duration: 400,
		easing: cubicOut
	});
    // let buttonColour = "blue";

    export let rects = [];
    export let processed = false;

    function handleFile(file) {
        if (file.type.match(/image.*/)) {
            let reader = new FileReader();
            reader.onload = function(e) {
                let img = document.getElementById('input');
                img.src = e.target.result;
                img.onload = () => {
                    console.log("Loaded")
                    showImage = true
                    updateCanvas();
                };
                updateCanvas();
            }
            reader.readAsDataURL(file);
        }
        else {
            console.log("File not supported!");
        }
    }

	function logTesseract() {
        // processButton.innerHTML = "";
		Tesseract.recognize(
			input,
			'eng',
			{ logger: (m) => {
                console.log(m); 
                if (m.status == "recognizing text") {
                    percentage.set(m.progress);
                }
            }
            }
		).then((data) => {
			console.log(data.data);
            processed = true;
			onResult(data.data);
            // processButton.innerHTML = "Processed";
            // processButton.style.color = "white";
		});
	}

	function onResult(res) {
		showBoxes(res);
        console.log(res)
        rects = res.words.map(r => r.bbox);
	}

	function showBoxes(res) {
		res.words.forEach(function (w) {
			var b = w.bbox;
            var c = w.confidence;

            if (c >= 0) {
                ioctx.strokeWidth = 2;

                ioctx.strokeStyle = 'red';
                ioctx.strokeRect(b.x0, b.y0, b.x1 - b.x0, b.y1 - b.y0);
                ioctx.beginPath();
                ioctx.moveTo(w.baseline.x0, w.baseline.y0);
                ioctx.lineTo(w.baseline.x1, w.baseline.y1);
                ioctx.strokeStyle = 'green';
                ioctx.stroke();
            }

			// octx.font = '20px Times';
			// octx.font = 20 * (b.x1 - b.x0) / octx.measureText(w.text).width + "px Times";
			// octx.fillText(w.text, b.x0, w.baseline.y0);
		});
	}

	onMount(() => {
		updateCanvas();

        document.body.addEventListener('drop', async function(e){
            e.preventDefault();
            e.stopPropagation();
            handleFile(e.dataTransfer.files[0]);
        });
	});

    function updateCanvas() {
        let img = document.getElementById('input');
        
        input_overlay.width = img.width;
		input_overlay.height = img.height;
		ioctx = input_overlay.getContext('2d');
        console.log(ioctx)
    }
</script>

<main ondragover="return false" class="border left-0 right-0 top-0 bottom-0 items-center">

	<div bind:this={div} id="img-container" class="">
		<canvas bind:this={input_overlay} id="input-overlay" class="toFit"/>
        <img
            bind:this={input}
            hidden={!showImage}
            id="input"
            class="toFit"
            alt="input"
        />
        <p hidden={showImage} class="toFit text-slate-500" id="text">
            Drag an Image to Start
        </p>
	</div>

    <!-- <div class="border-wrap" style="background: linear-gradient({90}deg, green {percentage}%, white {percentage}%)"> -->
    <div class="bottomLocation flex flex-col">
        <button on:click={logTesseract} 
            bind:this={processButton}
            class="inline-block px-6 py-2.5 bg-blue-600 text-white font-medium text-xs leading-tight uppercase rounded shadow-md hover:bg-blue-700 hover:shadow-lg focus:bg-blue-700 focus:shadow-lg focus:outline-none focus:ring-0 active:bg-blue-800 active:shadow-lg transition duration-150 ease-in-out"
        >
            Process Image
        </button>
        <progress class="w-full mt-2 h-1" value={$percentage} />
    </div>

	
    <!-- src="https://tesseract.projectnaptha.com/img/eng_bw.png" -->
</main>

